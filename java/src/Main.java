// create a hello world program in java

package src;

public class Main {
    public static void main(String[] args) {
        System.out.println("hello, java!");
        System.out.println("my name is abc0xmattyic333");
        System.out.println("i <3 computers and programming");
    }
}


// run the program to see the output

// the output should be: hello, java!

// congratulations! you have written your first java program.

// let's break down the program:

// 1. package src;
// this is a package declaration. a package is a namespace that organizes a set of related classes and interfaces.
// a namespace is a collection of classes and interfaces that are grouped together.

// 2. public class Main {}
// this is a class declaration. a class is a blueprint for objects.
// objects have states and behaviors. an object is created from a class.

// 3. public static void main(String[] args) {}
// this is a method declaration. a method is a block of code that performs a specific task.
// in this case, the main method is the entry point of the program.

// 4. System.out.println("hello, java!");
// this is a statement. a statement is a complete command to be executed.
// in this case, the statement prints the string "hello, java!" to the console.
// a string is a sequence of characters.
// a string can be enclosed in single quotes ('hello, java!') or double quotes ("hello, java!").
// the println method is used to print a line of text to the console.
